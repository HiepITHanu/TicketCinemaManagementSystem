    public void updateTotalPrice(){
        double totalPrice = 0;
        
        for(JLabel l : getTotalLabels()){
            if(l.getText().equals("")){
                continue;
            }else{
                totalPrice += Double.parseDouble(l.getText());
            }
        }
        
        this.totalOutput.setText(String.valueOf(totalPrice));
    }
    
